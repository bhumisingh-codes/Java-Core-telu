public class Loops{
    public static void main(String args[]) {
        //while loop
        int i=1; 
        while(i<=4){
            System.out.println("Hii"+i);
            i++;
        }
        //do-while loop
        int j=2;
        do{
            System.out.println("Hello"+j);
            j++;
        }
        while(j==1);
        //for loop
        for(int a=0; a<=3; a++){
            System.out.println("Hey"+a);
        }

    }
}